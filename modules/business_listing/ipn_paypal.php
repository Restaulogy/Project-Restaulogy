// read the post from PayPal system and add 'cmd'
$req = 'cmd=_notify-validate';

foreach ($_POST as $key => $value) {
	$value = urlencode(stripslashes($value));
	$req .= "&$key=$value";
}

// post back to PayPal system to validate
$header .= "POST /cgi-bin/webscr HTTP/1.0\r\n";
$header .= "Content-Type: application/x-www-form-urlencoded\r\n";
$header .= "Content-Length: " . strlen($req) . "\r\n\r\n";
$fp = fsockopen ('www.paypal.com', 80, $errno, $errstr, 30);

// assign posted variables to local variables
$item_name = $_POST['item_name'];
$item_number = $_POST['item_number'];
$listing_id = $_POST['custom'];
$payment_status = $_POST['payment_status'];
$payment_amount = $_POST['mc_gross'];
$payment_currency = $_POST['mc_currency'];
$txn_id = $_POST['txn_id'];
$receiver_email = $_POST['receiver_email'];
$payer_email = $_POST['payer_email'];

if (!$fp) {
	// HTTP ERROR
	$notice .= $language->desc('site_text',$lang_set,'paypal_error')." - error(100)<br>";
} else {
	fputs ($fp, $header . $req);
	while (!feof($fp)) {
		$res = fgets ($fp, 1024);
		if (strcmp ($res, "VERIFIED") == 0) {
			// check the payment_status is Completed
			if ($payment_status == "Completed"){
				// check that receiver_email is your Primary PayPal email
				if($receiver_email != $config['paypal_email']){
					$notice .= $language->desc('site_text',$lang_set,'paypal_error')." - error(101)<br>";
				}
				// check that payment_amount/payment_currency are correct
			}else{
				// process incomplete payment
				$notice .= $language->desc('site_text',$lang_set,'paypal_error')." - error(103)<br>";
			}
			// process payment
			if ($notice == ""){
				$pay_success = true;
			}
		}elseif (strcmp ($res, "INVALID") == 0) {
			// log for manual investigation
			$notice .= $language->desc('site_text',$lang_set,'paypal_error')." - error(104)<br>";
		}
	}
	fclose ($fp);
}

